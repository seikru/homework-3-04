def strUpper(str):
    """
    Возвращает строку с заглавными буквами

    """
    input_text = ("Введите строку: ")
    upper_text = input_text.upper()
    #    print(upper_text)
    return upper_text

def strTitle(str):
    """
    Переводит в заглавные первые буквы строки

    """
    input_text = ("Введите строку: ")
    title_text = input_text.title()
    #    print(title_text)
    return title_text
